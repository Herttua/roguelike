tilemap::~tilemap()
{
    SDL_DestroyTexture(grass);
    SDL_DestroyTexture(dirt);
    SDL_DestroyTexture(water);
}

tilemap::tilemap() 
{
    grass = texture::load_texture("assets/grass.png");
    dirt  = texture::load_texture("assets/dirt.png");
    water = texture::load_texture("assets/water.png");

    load(lvl1);
    src.x = 0;
    src.y = 0;
    src.w = 24;
    src.h = 24;
    
    dst.w = 24;
    dst.h = 24;
}

void tilemap::load(int arr[20][25])
{
    for(int row = 0; row < 20; row++)
    {
        for(int column = 0; column < 25; column++)
        {
            map[row][column] = arr[row][column];        
        }
    }
}

void tilemap::draw()
{
    int type = 0;
    for(int row = 0; row < 20; row++)
    {
        for(int column = 0; column < 25; column++)
        {
            type = map[row][column];
            
            dst.x = column * 24;
            dst.y = row * 24;
            
            switch(type)
            {
            case 0:
                texture::draw(grass, src, dst);
                break;

            case 1:
                texture::draw(dirt, src, dst);
                break;

            case 2:
                texture::draw(water, src, dst);
                break;
            
            default: break;
            }        
        }
    }
}